    public int countNegatives(int[][] grid) {
        if (grid.length == 0) {
            return 0;
        }

        int m = grid.length;
        int n = grid[0].length;
        int res = 0;

        int j = n - 1;
        for (int i = 0; i < m; i++) {
            for (; j >= 0; j--) {
                if (grid[i][j] >= 0) {
                    break;
                }

            }
            res += n - 1 - j;
        }
        return res;
    }


